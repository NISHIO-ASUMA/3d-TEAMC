#ifndef _EVENT_H_
#define _EVENT_H_

//**************************************************************************************************************
//インクルードファイル
//**************************************************************************************************************
#include"main.h"

//**************************************************************************************************************
// マクロ定義
//**************************************************************************************************************
#define EVENTPOS_ONE (D3DXVECTOR3(-616.0f, 0.0f, -690.0f)) // イベントの場所

//**************************************************************************************************************
//イベントの場所
//**************************************************************************************************************
typedef enum
{
	EVENTPOSITION_ONE = 0,
	EVENTPOSITION_TWO,
	EVENTPOSITION_THREE,
	EVENTPOSITION_FOUR,
	EVENTPOSITION_MAX
}EVENTPOSITION;

//**************************************************************************************************************
//イベントの構造体
//**************************************************************************************************************
typedef struct
{
	D3DXVECTOR3 Eventpos; // 位置
	bool bEvent;		  // イベントが始まっているか
	int nTime;			  // イベントの時間
	int IventPosition;    // イベントの場所
	int nCylinderIdx;     // シリンダーのインデックス
}Event;

//**************************************************************************************************************
//プロトタイプ宣言
//**************************************************************************************************************
void InitEvent(void); // イベントの初期化処理
void SetEvent(D3DXVECTOR3 Eventpos, int IventPosition,int nTime); // イベントの設定処理
void UpdateEvent(void); // イベントの更新処理
bool EnableEvent(void); // イベントが発生してるかどうか
int GetEventPos(void);  // イベントの発生地点の取得
#endif